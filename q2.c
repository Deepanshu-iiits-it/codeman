// program to read 2 no. and find sum of the square of 1st no. and cube of the 2nd no.
// example: input: 5 3
// output: 5^2 + 3^3 = 52

#include<stdio.h>
#include<math.h>
void main()
{
        int l,b;
        printf("\n enter the two numbers:");
        scanf("%d %d",&l,&b);
        printf("\n sum = %d",l*l+b*b*b);
}

