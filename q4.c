// program to delete the last digit of a no.
// example: input: 5234
// output: 523

#include<stdio.h>
#include<math.h>
void main()
{
        int l;
        printf("\n enter the number:");
        scanf("%d",&l);
        printf("\n the new no. after deleting the last digit = %d",l/10);
}

